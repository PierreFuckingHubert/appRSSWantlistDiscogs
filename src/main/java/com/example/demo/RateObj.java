package com.example.demo;

public class RateObj {

    int rate;
    String raters;



    public RateObj(int rate, String raters) {
        this.rate = rate;
        this.raters = raters;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public String getRaters() {
        return raters;
    }

    public void setRaters(String raters) {
        this.raters = raters;
    }
}
